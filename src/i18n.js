export function getUiLanguage() {
  const languages = typeof navigator !== 'undefined'
    ? (navigator.languages?.length ? navigator.languages : [navigator.language])
    : []
  return languages.some(language => String(language || '').toLowerCase().startsWith('zh')) ? 'zh' : 'en'
}

export const uiLanguage = getUiLanguage()

function lookup(dict, key) {
  return key.split('.').reduce((value, part) => (
    value && Object.prototype.hasOwnProperty.call(value, part) ? value[part] : undefined
  ), dict)
}

export function t(key, params = {}) {
  const value = lookup(dictionaries[uiLanguage], key) ?? lookup(dictionaries.en, key) ?? key
  return String(value).replace(/\{(\w+)\}/g, (_, name) => (
    params[name] == null ? '' : String(params[name])
  ))
}

export function isChineseUi() {
  return uiLanguage === 'zh'
}

export function formatBytes(bytes) {
  if (!bytes) return t('common.unknownSize')
  const units = ['B', 'KB', 'MB', 'GB']
  let value = bytes
  let unitIndex = 0
  while (value >= 1024 && unitIndex < units.length - 1) {
    value /= 1024
    unitIndex += 1
  }
  return `${value.toFixed(value >= 10 || unitIndex === 0 ? 0 : 1)} ${units[unitIndex]}`
}

export function formatDuration(seconds) {
  if (!seconds) return uiLanguage === 'zh' ? '0秒' : '0s'
  const minutes = Math.floor(seconds / 60)
  const rest = (seconds % 60).toFixed(1)
  if (uiLanguage === 'zh') {
    return minutes > 0 ? `${minutes}分${rest}秒` : `${rest}秒`
  }
  return minutes > 0 ? `${minutes}m ${rest}s` : `${rest}s`
}

export function formatDateTime(value) {
  if (!value) return t('common.unknown')
  return new Intl.DateTimeFormat(uiLanguage === 'zh' ? 'zh-CN' : 'en-US', {
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
    hour: '2-digit',
    minute: '2-digit',
  }).format(new Date(value))
}
